                国内5G研发年投入超30亿元<br />
                <br />
                爱立信，这家于1876年成立于瑞典首都斯德哥尔摩的移动通信设备商，在2G、3G和4G时代进行着广泛的商用部署，并在移动通信方面有着全球领先的称号。<br />
                <br />
                在5G时代争夺市场环节中，爱立信一度实现了反超，曾拿下了全球第一的宝座——根据今年4月国外调查机构数据，爱立信以24%的5G市场份额全球登顶，三星、诺基亚、华为等位居其后。<br />
                <br />
                这也再度引发大家对爱立信的讨论。<br />
                <br />
                在赵钧陶看来，爱立信已在中国“扎根”百余年，是唯一一家经历了中国从1G到5G移动发展全过程的设备商。近几年爱立信扩大了在中国的研发力量，研发工程师超5500人，其中绝大部分从事与5G相关工作，包括解决方案、产品、硬件开发等。<br />
                <br />
                “我们平均每年研发投入超过30亿元人民币，这使得爱立信在中国的研发中心成为了在中国独立运营的最大的跨国企业研发中心”，赵钧陶说，已有大量员工在中国、韩国、日本以及其它地区接受了培训，目的是全力支持中国运营商部署5G网络。<br />
                <br />
                预计今年底全球5G用户破千万<br />
                <br />
                “我们预测到今年年底，全球的5G用户可以超过1000万，在历代移动通信发展历史上，这个扩张速度是前所未有的”，他举例称，韩国在5G网络商用发布的第69天之后便达到了100万用户数，而明年各国也将陆续迎来“大爆发”。<br />
                <br />
