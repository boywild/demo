                AI+5G将成为重要增长点<br />
                <br />
                赵钧陶认为:“人工智能和5G的结合，将是一个新的领域，而且是一个重要的增长点”。他还表示，5G的发展可以从两方面分析，首先对于消费者而言，5G将会成为更多新技术的基础平台和技术支撑。无论是与AI、VR、AR的结合，还是与无人驾驶等技术的结合，都会给消费者带来革命性的体验；其次，5G将渗透到各个垂直行业，例如工业互联网、工业数字化转型等，“很大的增长点将发生在产业应用方面，但是这需要两方面的融合，更需要时间”。<br />
                <br />
                面对行业竞争，赵钧陶表示，这是促进企业健康成长的必要外界因素。他说：“我们欢迎竞争，但从我们自身来讲，会更多地将注意力放到自己的事情上。为客户带来最大价值，不管竞争条件如何，不管对手多么强大，我们都能够在竞争中生存下来，壮大起来”。<br />
                <br />
                以下为新浪科技对话赵钧陶实录：<br />
                <br />
                新浪科技：5G商用牌照的发放，对中国5G整体发展有何影响？<br />
                <br />
                赵钧陶：感谢新浪的朋友们。中国5G牌照在六月初正式发放，这件事是产业界的重大利好，而且这个利好不仅对中国产业有影响，也会对全球的5G产业有影响。这个影响主要来自如下几个方面：<br />
                <br />
                第一，中国的市场规模会对全球的5G生态系统带来重大的影响。<br />
                <br />
                第二，创新。中国在移动通信产业整个创新系统上非常活跃，而且给全球带来了很多新的创新，这也刺激了中国的经济发展。我们在过去几年对5G开展了很多宣传，而5G的潜力会随着商用在更多的领域发挥更大的作用，所以中国的5G牌照正式发放不仅影响中国，也影响全球。从产业，制造业，从爱立信公司本身来讲，我们期盼已久，我们为这件事情已经做了几年的准备。现在中国加入了全球5G商用的行列，我想对爱立信本身来讲也是具有很好的意义，因为我们十分看好中国的市场。<br />
                <br />
                新浪科技：牌照的提前发放，意味着什么？<br />
                <br />
                赵钧陶：如果做解读，我想中国有关产业管理部门，也可能运营商的朋友们会更权威一点。从爱立信来讲，我们觉得提前发放牌照会提前释放整个产业的活力，从时间的角度来讲，对产业链上的每个参与者都是一个激励，使大家全力以赴来做这件事。提前发放也标志着中国5G的发展两步并成了一步。因为原来在讲先发预商用或临时牌照，现在直接发放商用牌照，这是一个重大的利好，会使中国5G的商用化进程大大加快。<br />
                <br />
                新浪科技：爱立信为迎接5G商用，准备如何？<br />
                <br />
                赵钧陶：爱立信在这几年为5G做了大量的准备，我们确实是盼着这一天的到来。从中国移动通信发展的整个进程来讲，爱立信是唯一一家经历了整个行业发展的全过程的设备提供商，这个全过程是指从第一代移动通信到第五代移动通信，完完整整参与、建设和贡献。作为一个亲历者，贡献者，受益者，我们一定全力以赴。爱立信的准备也从各个方面，如生产供应方面，我们在中国有移动网络生产厂，我们在前几年就做了5G生产线的更新换代。从研发的角度来讲，这几年我们做了很多投资，而且壮大了我们在中国的研发队伍。我们现在在中国有超过5500名研发工程师，其中绝大部分是在从事与5G相关的解决方案和产品，包括硬件的开发。去年，在北京市政府的支持下，我们在望京科技园建立了全球最先进的毫米波无线电的测试实验室。从交付队伍来讲，我们这些年也做了充分的准备，我们自己的交付队伍和合作伙伴进行5G相关支持，我们送很多员工到国外去感受先行国家的5G网络。<br />
                <br />
                新浪科技：目前与国内运营商进展如何？<br />
                <br />
                赵钧陶：我们遵循整个运营商的时间点，每个运营商的时间点略有差异。从我们现在看到的情况来讲，各个运营商都在全力以赴在落实我们刚才说的预商用的网络，而且他们有清晰的时间表，从我们来讲，我们动用全部的力量来积极配合和支持运营商的时间表，完成我们的责任。<br />
                <br />
                新浪科技：中国广电的入局，挑战更大还是机遇更大？<br />
                <br />
                赵钧陶：这是5G发放商用牌照另外一个大家所关注的事件。传统上来讲，中国只有三家电信运营商，这次发了第一张移动牌照给中国广电运营商，从爱立信的角度讲是一个大的利好，对于中国产业融合来讲是一个利好。全球的情况下，移动运营商和广电运营商的界限越来越模糊了。我们现在看到，三网融合在全球许多地方这是实实在在发生的事情。在中国我们也看到，这几年电信运营商已经发展的非常快。我们觉得无论从网络规模还是从良性竞争，抑或是从消费者获得更大的好处的角度来看，这都是一个利好。<br />
                <br />
                爱立信本身来讲，我们一直和广电领域有合作，而且是向全球包括中国的广电运营商提供了大量先进的解决方案。其经验与产品是爱立信在传统的移动通信设备供应商中所独有的优势。爱立信不仅有技术领先的5G系统，而且我们在广电行业提供了最先进的编转码设备和内容分发平台。近些年并多次获得广播电视的崇高荣誉奖，艾美技术工程奖，为4K和8K等高清视频在移动手持设备的高质量和高效率传输做出了突出贡献。我们可以向运营商提供独一无二的、端到端的解决方案，就是我们现在讲的5G加上4K或者8K，加上VR的解决方案。<br />
                <br />
                新浪科技：5G大规模商用还需要多久？<br />
                <br />
                赵钧陶：爱立信刚刚出了最新一期的《爱立信移动市场报告》，在这个报告里我们谈到，期待明年全球大规模商用开始，但是从今年来看，也是很振奋人心的。爱立信预测到今年年底，全球的5G用户可以超过1000万，在历代移动通信发展历史上，是没有这个扩张速度的。我们看到在韩国在网络发布69天之后就达到100万，明年为什么会说是大规模开始商用，有几个方面：一个是会有更多的国家和市场参与到5G的商用队伍中来，还有一个，明年会有更多的5G手机、半导体器件，还有5G的终端设备面世。会给消费者带来更多的选择。我们觉得明年会是一个比较大的爆发。今年我们讲是5G的商用元年，不仅在中国，也在全球。<br />
                <br />
                新浪科技：5G终端价格，何时才能被大众接受？<br />
                <br />
                赵钧陶：任何一项新技术都要经历早期到普及的过程，这当然也包括移动产业。4G手机、3G手机实际上都经历了这样的过程。无论是从最初测试到大规模的商用，还是从芯片工厂到手机工厂，产业都做了大量的工作。我们看到，整个5G商用化过程和规模化过程都要比前几代移动通信快得多。所以我觉得在未来的两年或者三年内，手机的价格会有比较大幅度的下降，从而可能会被更多的消费者所接受。<br />
                <br />
                从市场热情的角度看，刚刚提到5G手机现在还比较贵。不过以韩国为例，在韩国已经有100万人成为5G的注册用户，这也意味着这100万用户将会得到新的5G手机。在市场上，尽管现在（5G手机）价格还比较贵，但还是有很多热情的消费者愿意尝试5G新技术所带给他们的前所未有的体验。<br />
                <br />
                新浪科技：爱立信在国内研发5G投入多少？<br />
                <br />
                赵钧陶：为了备战5G，爱立信这几年在中国做了很大的投入。这也是爱立信集团对中国市场长期承诺的一部分。最近这几年我们每年在中国有30亿的固定投入来支持研发费用，支持中国研发团队所承担的包括5G在内的全球研发任务。<br />
                <br />
                新浪科技：近期有报告透露，5G人才薪资为1.4万。我国应如何培养5G人才？<br />
                <br />
                赵钧陶：人才的价值由市场决定。我虽然不知道每个企业5G工程师的具体薪资如何，但这一定是和移动通信的整个趋势相符合的，也是和高新技术企业发展相符合的。因为5G是辐射和影响特别广的一个产业，我们现在所谈的5G不仅是5G本身，还包括从5G衍生的各种各样的应用，以及5G和其他产业的交叉融合。我觉得那些真正有才华，有知识的人才，在市场上的发展是不可限量的。<br />
                <br />
                人工智能和5G的结合是一个新的领域，而且是一个重要的增长点。大家都特别看好这个领域。又比如5G和大数据，我们知道5G会产生极大的数据量。本身人工智能和大数据人才就在全球范围内短缺，而如果这些人才能够将他们的知识与5G结合起来，我个人觉得他们的薪酬不止1.4万，可能是2.4万或者更高。<br />
                <br />
                新浪科技：5G带来最明显的改变将发生在哪些行业？<br />
                <br />
                赵钧陶：我觉得需要从两个方面看5G。过去消费者时代的通信主要在和人打交道，而未来（的通信）将会影响社会和产业。<br />
                <br />
                一方面是5G将使消费者的体验大大提升，因为5G将会成为更多新技术的基础平台和技术支撑。比如我们所谈到的人工智能，虚拟现实，增强现实技术与5G的结合，5G无人驾驶汽车和无人机等等，都会产生很多新的东西，而这些新的东西会给消费者带来革命性的体验。这个方面会继续发展，而且现在已经有了不错的前景。比如韩国（的5G发展），其实目前就主要是在增强消费者体验。<br />
                <br />
                另外一个方面是5G将渗透到各个垂直行业。我们现在所谈到的工业互联网和工业数字化转型里，有很多应用场景是在现在这个时间点不可能完全想象的。除了传统消费领域，未来一个很大的增长点将会是在产业应用方面。<br />
                <br />
                不过大家切记，这一切都需要时间，因为这需要两方面的支持和融合。<br />
                <br />
                新浪科技：如何评价爱立信与其他5G厂商的竞合关系？<br />
                <br />
                赵钧陶：大家都知道爱立信1876年在斯德哥尔摩建立，是一家具有百年历史的公司。在当今的通信历史上，没有一家通信领域的公司像爱立信这么长久。而且，从第一代移动通信到第五代移动通信，爱立信是唯一的一家经历了中国乃至全球的移动通信发展全过程的公司。从竞争来讲，爱立信一百年的历史，实际上就是一个竞争的历史。爱立信来自于瑞典这个只有一千多万人口国家，但是能在一百多年的大浪淘沙之中生存下来。我们是欢迎健康的竞争的。我们一直认为，竞争是使我们整个产业具有活力的一个重要的基础，也是一家公司能够健康成长的一个必不可少的外界因素，所以我们欢迎竞争。从我们自己的角度来讲，我们更多的注意力需要放在做好自己的事情上。自己的事情就是指那些能够给我们的客户带来最大的价值的事情。一百多年来，我们一直关注自身，努力做好自己的事情，为我们的客户带来最大的价值，从而提升我们的用户满意度。因此，无论竞争条件如何，无论对手多么强大，我们始终能够在激烈竞争中生存下来，壮大起来。							</div>
