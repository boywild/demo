                <br />
                “无论是商用化的过程和规模化的过程，都要比前几代移动通信发展更快。所以我认为在未来两年或者三年，手机的价格会出现大幅度下降，会被更多的消费者接受。当然，即便现在价格较贵，也依旧有很多人愿意体验”，赵钧陶说道。							</div>
